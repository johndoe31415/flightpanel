static int codepoint_to_charindex(const unsigned int codepoint) {
	if (codepoint == 32) {
		return codepoint - 32;
	} else if (codepoint == 46) {
		return codepoint - 45;
	} else if ((codepoint >= 48) && (codepoint <= 57)) {
		return codepoint - 46;
	} else if (codepoint == 65) {
		return codepoint - 53;
	} else if (codepoint == 70) {
		return codepoint - 57;
	} else if ((codepoint >= 79) && (codepoint <= 80)) {
		return codepoint - 65;
	} else {
		return -1;
	}
}

