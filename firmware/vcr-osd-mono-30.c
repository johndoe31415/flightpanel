static int codepoint_to_charindex(int codepoint) {
	if (codepoint == 46) {
		return codepoint - 46;
	} else if ((codepoint >= 48) && (codepoint <= 57)) {
		return codepoint - 47;
	} else {
		return -1;
	}
}

