#include <stdint.h>
#include <misc.h>
#include "stm32f4xx_reset.h"

void stm32f4xx_reset(void) {
  SCB->AIRCR = (0x5fa << SCB_AIRCR_VECTKEY_Pos) | (1 << SCB_AIRCR_SYSRESETREQ_Pos);
}
